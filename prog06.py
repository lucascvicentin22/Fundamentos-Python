# Operadores de comparação 

if __name__ == "__main__":
    
    # Igual (==)
    # Verfica se o valor da esquerda é igual ao valor da direita 
    print(20 == 19)

    # Diferente (!=)
    # Verifica se o valor da esquerda é diferente do valor da direita
    print ("Chegado" != "Não_Chegado")

    # Maior que (>)
    #Verifica se o valor da esquerda é maior que o valor da direita
    print(4 > 3)

    # Menor que (<)
    # Verifica se o valor da esquerda é maior do que o valor da direita
    print(10 < 13)

    # Maior ou igual a (>=)
    # Verifica se o valor da esque é maior ou igual ao valor da direita
    print(5>= 5)

    #Manor ou igual a (<=)
    # Verifica se o valor da esquerda é menor ou igual ao valor da direita
    print(8 <= 7)
