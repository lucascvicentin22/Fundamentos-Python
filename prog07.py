# Operadores Lógicos

if __name__ == "__main__":

    # Operador AND
    # Retorna True caso dois lados de comparação retornem True. Caso contrário, retorna false
    print(10 > 6 and 4 > 3)

    # Operador OR
    # Retorna True caso qualquer  um dos lados de comparação retorna True. Caso contrário
    # retorna false
    print(10 < 90 or 5 >= 5)

    # Operador NOT
    # Retorna a negação de um valor. Caso o valor for True, Retorna False. Caso 
    # o valor seja False, retorna True
    print(not 10 > 11)
    
